// 获取拼音候选词
export function getPinyinCandidates(pinyin: string, limit: number = 9): string[] {
  if (!pinyin) return [];
  
  const py = pinyin.toLowerCase().trim();
  
  // 精确匹配
  if (PINYIN_DICT[py]) {
    return PINYIN_DICT[py].slice(0, limit);
  }
  
  // 前缀匹配
  const candidates: string[] = [];
  for (const [key, chars] of Object.entries(PINYIN_DICT)) {
    if (key.startsWith(py)) {
      candidates.push(...chars.slice(0, 3));
      if (candidates.length >= limit) break;
    }
  }
  
  return candidates.slice(0, limit);
}

// 检查是否是有效的拼音前缀
export function isValidPinyinPrefix(str: string): boolean {
  if (!str) return false;
  const py = str.toLowerCase();
  
  // 检查是否有匹配的拼音
  for (const key of Object.keys(PINYIN_DICT)) {
    if (key.startsWith(py) || py.startsWith(key)) {
      return true;
    }
  }
  return false;
}
